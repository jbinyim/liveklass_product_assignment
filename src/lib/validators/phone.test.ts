import { describe, it, expect } from "vitest";
import { isKoreanPhone } from "./phone";

describe("isKoreanPhone", () => {
  describe("통과", () => {
    it.each([
      ["010-1234-5678", "전형적 모바일 8자리"],
      ["01012345678", "하이픈 없음"],
      ["010 1234 5678", "공백 구분"],
      ["011-123-4567", "구형 모바일 7자리"],
      ["02-123-4567", "서울 7자리"],
      ["02-1234-5678", "서울 8자리"],
      ["031-123-4567", "경기 7자리"],
      ["070-1234-5678", "인터넷 전화"],
    ])("%s (%s)", (input) => {
      expect(isKoreanPhone(input)).toBe(true);
    });
  });

  describe("실패", () => {
    it.each([
      ["", "빈값"],
      ["abc-1234-5678", "문자 포함"],
      ["010-12-3456", "모바일 길이 부족"],
      ["010-12345-6789", "모바일 길이 초과"],
      ["123-456-7890", "미국식"],
      ["+82 10 1234 5678", "국제표기"],
      ["   ", "공백만"],
    ])("%s (%s)", (input) => {
      expect(isKoreanPhone(input)).toBe(false);
    });
  });
});
